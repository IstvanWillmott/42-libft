#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == (unsigned char )c)
			break ;
		i--;
	}
	if (i < 0)
		return (NULL);
	else
		return ((char *) s + i);
}
